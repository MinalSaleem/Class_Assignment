# string

name = " Ariba"
print(name, type(name))


# integer

number = 13
print(number, type(number))


# float

float_number = 2.7
print(float_number, type(float_number))



# boolean

boolean_value = True
print(boolean_value, type(boolean_value))


# list

list_values = ["minal" , "darakhshan"]
print(list_values, type(list_values))


# tuple

tuple_values = ("minal" , "darakhshan")
print(tuple_values, type(tuple_values))


# set

set_values = {"minal" , "darakhshan"}
print(set_values, type(set_values))


# dictionary

dict_values = {"name" : "minal" , "age" : 18}
print(dict_values, type(dict_values))


# NoneType

none_value = None
print(none_value, type(none_value))